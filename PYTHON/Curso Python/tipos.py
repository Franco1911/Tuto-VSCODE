#Siempre que trabajemos con numeros enteros o flotantes podemos usar operaciones aritmenticas.
#Int, nos permite trabajar con muneros enteros positivos y enteros negativos.
numero_uno = 10, -10
print(numero_uno)

numero_uno = 10 * 10
print(numero_uno)

numero_uno = 10 - 10
print(numero_uno)

numero_uno = 10 + 10
print(numero_uno)

numero_uno = 10 / 10
print(numero_uno)
#Al dividir con / se obtiene tipo de dato flotante.
#Al dividir con // se obtiene tipo de dato entero.
numero_uno = 10 // 3
print(numero_uno)


#Float, no permite almacenar datos de tipo flotante o sea numeros decimales (+) y (-).
numero_dos = 3.14, -3.14
print(numero_dos)

#Bool, solo puede almacenar dos valores "verdadero" y "falso". Siempre True y False en mayusculas.
